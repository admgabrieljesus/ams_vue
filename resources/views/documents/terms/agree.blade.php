        <h2><b>FINALIDADES DO TRATAMENTO DOS DADOS</b></h2>
        <p>O tratamento dos dados pessoais, listados neste termo, tem as seguintes finalidades:</p>
        <ul>
            <li>
                <p>Possibilitar que a <b>AEPET-BA</b> identifique e entre em contato com o <b>Titular</b> para fins de
                    relacionamento no que diz respeito aos objetivos do Estatuto da <b>AEPET-BA</b>.</p>
            </li>
            <li>
                <p>A <b>AEPET-BA</b> fica autorizado a compartilhar os dados pessoais e dados pessoais sensíveis do
                    <b>Titular</b> com outros agentes de tratamento de dados, caso seja necessário para as finalidades
                    listadas neste termo, particularmente no atendimento aos objetivos do Estatuto da <b>AEPET-BA</b>
                    para ações judiciais, extrajudiciais e arbitrais e o contato informativo, observados os princípios e
                    as garantias estabelecidas pela Lei nº 13.709 (LGPD).</p>
            </li>
        </ul>

        <h2><b>SEGURANÇA DOS DADOS</b></h2>
        <p>A <b>AEPET-BA</b> responsabiliza-se pela manutenção de medidas de segurança, técnicas e administrativas aptas
            a proteger os dados pessoais de acessos não autorizados e de situações acidentais ou ilícitas de destruição,
            perda, alteração, comunicação ou qualquer forma de tratamento inadequado ou ilícito.</p>
        <p>Em conformidade ao art. 48 da Lei nº 13.709, A <b>AEPET-BA</b> comunicará ao <b>Titular</b> e à Autoridade
            Nacional de Proteção de Dados (ANPD) a ocorrência de incidente de segurança que possa acarretar risco ou
            dano relevante ao <b>Titular</b>.</p>

        <h2><b>DIREITOS DO TITULAR</b></h2>
        <p>O <b>Titular</b> tem direito a obter da AEPET-BA, em relação aos dados por ela tratados, a qualquer momento e
            mediante requisição:</p>
        <ul>
            <li>
                <p>confirmação da existência de tratamento;</p>
            </li>
            <li>
                <p>acesso aos dados;</p>
            </li>
            <li>
                <p>correção de dados incompletos, inexatos ou desatualizados;</p>
            </li>
            <li>
                <p>anonimização, bloqueio ou eliminação de dados desnecessários, excessivos ou tratados em
                    desconformidade com o disposto na Lei nº 13.709;</p>
            </li>
            <li>
                <p>portabilidade dos dados a outro fornecedor de serviço ou produto, mediante requisição expressa e
                    observados os segredos comercial e industrial, de acordo com a regulamentação do órgão controlador;
                </p>
            </li>
            <li>
                <p>portabilidade dos dados a outro fornecedor de serviço ou produto, mediante requisição expressa, de
                    acordo com a regulamentação da autoridade nacional, observados os segredos comercial e industrial;
                </p>
            </li>
            <li>
                <p>eliminação dos dados pessoais tratados com o consentimento do <b>Titular</b>, exceto nas hipóteses
                    previstas no art. 16 da Lei nº 13.709; VII – informação das entidades públicas e privadas com as
                    quais o controlador realizou uso compartilhado de dados;</p>
            </li>
            <li>
                <p>informação sobre a possibilidade de não fornecer consentimento e sobre as consequências da negativa;
                </p>
            </li>
            <li>
                <p>revogação do consentimento, nos termos do § 5º do art. 8º da Lei nº 13.709.</p>
            </li>
        </ul>


